package com.example.demo.DTO;

public abstract class ParticipantDTO {
    private int id;
    private String name;

    public ParticipantDTO(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
